// Utility function to get currency info
function getCurrencyInfo(code) {
    return CURRENCY_DATA[code] || { symbol: code, name: 'Unknown Currency' };
}

// Utility function to format currency with proper symbol
function formatCurrencyWithCode(amount, currencyCode = 'USD') {
    const currency = getCurrencyInfo(currencyCode);
    const formattedAmount = amount.toLocaleString('en-US', { minimumFractionDigits: 2, maximumFractionDigits: 2 });
    return `${currency.symbol}${formattedAmount}`;
}
